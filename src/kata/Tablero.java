    private void SigPartidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SigPartidaMouseClicked
        AnotaJ1.setEnabled(true);
        AnotaJ2.setEnabled(true);
        Empezar.setEnabled(false);
        this.Text1.setText("");
        this.Text2.setText("");
        Jugador2.setScore(0);
        Jugador1.setScore(0);
    }//GEN-LAST:event_SigPartidaMouseClicked

    private void FinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FinMouseClicked
        Empezar.setEnabled(false);
        AnotaJ1.setEnabled(false);
        AnotaJ2.setEnabled(false);
        SigPartida.setEnabled(false);
        if (puntoJ1>puntoJ2){
            JOptionPane.showMessageDialog(rootPane, Jugador1.getNombre()+" ha ganado el juego.");
        } else if (puntoJ1<puntoJ2){
            JOptionPane.showMessageDialog(rootPane, Jugador2.getNombre()+" ha ganado el juego.");
        } else if (puntoJ1==puntoJ2){
            JOptionPane.showMessageDialog(rootPane, "DEUCE!!! \n"
                    + " "+Jugador1.getNombre() +" y "
                    + " "+Jugador2.getNombre() +" han quedado empatados." );
        }
    }//GEN-LAST:event_FinMouseClicked

