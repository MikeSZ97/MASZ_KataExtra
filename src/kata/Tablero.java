    private void AnotaJ1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AnotaJ1MouseClicked
        // TODO add your handling code here:
        anota(Jugador1);
        if (Jugador1.getScore()<=40){
        this.Text1.setText(Jugador1.getScore()+ "");
        } else {
            this.Text1.setText("40");
            Jugador1.setScore(40);
        }
        if (Jugador1.getScore()==40){
        JOptionPane.showMessageDialog(rootPane, Jugador1.getNombre()+" ha ganado la partida " + contador);
        contador++;
        puntoJ1++;
        AnotaJ1.setEnabled(false);
        AnotaJ2.setEnabled(false);
        Empezar.setEnabled(false);
        SigPartida.setEnabled(true);
        Fin.setEnabled(true);
        }
    }//GEN-LAST:event_AnotaJ1MouseClicked

